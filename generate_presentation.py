from pptx import Presentation
from pptx.util import Inches, Pt

# إنشاء عرض تقديمي جديد
prs = Presentation()

# دالة لإضافة شريحة بعنوان ومحتوى وملاحظات التصميم
def add_slide(title, content, design_note):
    slide_layout = prs.slide_layouts[1]  # تخطيط "العنوان والمحتوى"
    slide = prs.slides.add_slide(slide_layout)
    title_placeholder = slide.shapes.title
    title_placeholder.text = title

    # إعداد المحتوى كنقاط
    content_box = slide.placeholders[1]
    tf = content_box.text_frame
    tf.clear()
    for line in content.strip().split("\n"):
        p = tf.add_paragraph()
        p.text = line.strip()
        p.font.size = Pt(18)
        p.level = 0

    # إضافة ملاحظات التصميم والانتقالات في مذكرة الشريحة
    notes_slide = slide.notes_slide
    notes_tf = notes_slide.notes_text_frame
    notes_tf.text = f"Design & Transition Suggestions:\n{design_note}\n"

# الشريحة 1: العنوان (تخطيط العنوان)
